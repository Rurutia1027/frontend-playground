// While Loops 
var names = ['Sebby', 'Sam', 'Robin', "Abgile", 'Peter', 'Willy']; 

while (false) { 
    console.log("this content will never be printed to the console!"); 
}


var number = 0; 
while (number < 4) { 
    console.log("number : " + number); 
    number++;  
}


var condition = true; 

while (condition) { 
    console.log("hello"); 
    condition = false; 
}



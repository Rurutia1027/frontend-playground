// Comparison Operators
// <, <=, >=, >, !=
// boolean is either true or false

// true
console.log(10 == 10);

// true
console.log(10 <= 10);

// true
console.log(10 >= 10);

// false
console.log(10 < 10);

// false
console.log(10 > 10);

// false
console.log(10 != 10);

// true
console.log("A" != "B");
